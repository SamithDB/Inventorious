    private void btn_nextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_nextActionPerformed
        
        if (next[1]) {
            
            if (newpro1.isNotEmpty()) {
                boolean yes=false;                
                try {
                    ResultSet r = new JDBC().getData("SELECT item_des FROM items WHERE item_des ='"+newpro1.getDescription()+"' ");
                    if (r.next()) {
                       int cont = JOptionPane.showConfirmDialog(this, "This item is already in your database !\nDo you need to continue ?","Build PC",0);
                        if (cont==0) {
                            yes=true;
                        } else {
                            yes=false;
                        } 
                    }else{
                        yes=true;
                    }
                } catch (Exception e) {
                    e.printStackTrace();
                }
                if (yes) {
                    newpro2.setDetails(newpro1.getModel().toUpperCase(),newpro1.getCategory().toUpperCase(),newpro1.getDescription().toUpperCase());
                    panel_Window.removeAll();
                    panel_Window.add(newpro2);
                    newpro2.setSize(443, 367);
                    newpro2.setVisible(true);
                    panel_Window.repaint();

                    btn_back.setEnabled(true);
                    btn_next.setText("Next");

                    back[0]=true;
                    next[1]=false;
                    next[2]=true;  
                }
                
            } else {
                JOptionPane.showMessageDialog(this, "Please Fill All Fields !");
            }

        }else if(next[2]){
            NewPro3 newpro3 = new NewPro3();
            String currentID = genItemID();
            try {                
                new JDBC().putData("INSERT INTO items VALUES('"+currentID+"','"+newpro1.getCategory().toUpperCase()+"','"+newpro1.getModel().toUpperCase()+"','"+newpro1.getDescription().toUpperCase()+"') ");
                newpro3.setID(currentID);
                panel_Window.removeAll();
                panel_Window.add(newpro3);
                newpro3.setSize(443, 367);
                newpro3.setVisible(true);
                panel_Window.repaint();

                item = currentID+"Â "+newpro1.getDescription().toUpperCase();
                btn_back.setEnabled(false);
                btn_cancel.setEnabled(false);
                next[2]=false;
                next[3]=true;
                btn_next.setText("Finish");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error while adding New item");
                e.printStackTrace();
            }

        }else if(next[3]){
            DefaultComboBoxModel model1 = (DefaultComboBoxModel) AddNewStockItems.item_ID.getModel();
            model1.addElement(item);
            AddNewStockItems.item_ID.setSelectedItem(item);
            dispose();
            AddNewStockItems.spin_qty.grabFocus();
        }
    }//GEN-LAST:event_btn_nextActionPerformed

