    String genItemID(){
        int num=0;
        try {
            
            ResultSet r = new JDBC().getData("SELECT COUNT(item_id) AS total FROM items");
            if (r.next()) {
                num = r.getInt("total")+1;
            }else{
                num=1;
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        return "S"+String.format("%05d",num);
    }
