    private void btnSendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendActionPerformed

        if (option.equals("email")) {
            final Socket sock = new Socket();
            final InetSocketAddress add = new InetSocketAddress("www.google.com", 80);
            try {
                sock.connect(add, 3000);
            } catch (IOException ex) {
                Logger.getLogger(NewMessage.class.getName()).log(Level.SEVERE, null, ex);
            }
            if (sock.isConnected()) {
                new Thread(new Runnable() {

                    @Override
                    public void run() {
                        try {
                            sendEmail.sendEmail("thusitha.blade@gmail.com", "T0771844682DJ", txtTo.getText(), txtSubject.getText(), txtMessage.getText());
                            JOptionPane.showMessageDialog(null, "Message Sent");
                           
                        } catch (Exception e) {
                            e.printStackTrace();
                        }
                    }
                }).start();
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Please connect to the internet");
            }
        } else {
            try {
                new JDBC().putData("INSERT INTO message(sender,receiver,message,m_status,datetime) VALUES('"+Home.userName+"','"+cmbTo.getSelectedItem()+"','"+txtMessage.getText()+"','Not Read','"+new Date().getMonth()+"/"+new Date().getDate()+" at "+Home.lblClock.getText()+"')");
                JOptionPane.showMessageDialog(this, "Message Sent");
                this.dispose();
            } catch (Exception e) {
                e.printStackTrace();
            }

        }
    }//GEN-LAST:event_btnSendActionPerformed

