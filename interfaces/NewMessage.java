    public void getUserNames() {
        Vector v = new Vector();
        try {
            ResultSet r = new JDBC().getData("SELECT userName FROM user");
            while (r.next()) {
                v.add(r.getString("userName"));
            }
            cmbTo.setModel(new DefaultComboBoxModel(v));
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

