    public void selectUser() {
        if (level.equals("admin")) {

        }
        if (level.equals("finance manager") || level.equals("manager")) {
            lblInvoice.setVisible(false);
            lblCustomer.setVisible(false);

        } else if (level.equals("stock handler")) {
            lblInvoice.setVisible(false);
            lblEmployee.setVisible(false);
            lblCustomer.setVisible(false);
            lblSupplier.setVisible(false);
            lblFinance.setVisible(false);

        } else if (level.equals("cashier")) {
            lblSupplier.setVisible(false);
            lblFinance.setVisible(false);
            lblEmployee.setVisible(false);

        } else if (level.equals("guest")) {
            lblSupplier.setVisible(false);
            lblFinance.setVisible(false);
            lblEmployee.setVisible(false);
            lblInvoice.setVisible(false);
        }
    }

