    public void panelEnter() {
        new Thread(new Runnable() {

            @Override
            public void run() {
                try {

                    for (int i = 500; i < 620; i++) {
                        jPanel1.setLocation(i, 110);
                        Thread.sleep(1);
                        if (i == 600) {
                            jPanel1.setVisible(true);
                        }
                    }
                    Thread.currentThread().stop();
                } catch (Exception e) {
                    e.printStackTrace();
                }
            }
        }).start();
    }

