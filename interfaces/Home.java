    private void lblStockMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblStockMouseClicked

        if (!(jPanel1.isVisible())) {
            jDesktopPane1.removeAll();
            panelEnter();
            jDesktopPane1.add(stock);
            stock.setSize(630, 490);
            stock.setLocation(0, 0);
            stock.setVisible(true);
            title = "Stock";

        } else if (jPanel1.isVisible() && !(title.equals("Stock"))) {
            jDesktopPane1.removeAll();
            panelExit();
            panelEnter();
            jDesktopPane1.add(stock);
            stock.setSize(630, 490);
            stock.setLocation(0, 0);
            stock.setVisible(true);
            title = "Stock";

        } else if (jPanel1.isVisible() && (stock.isVisible())) {
            jDesktopPane1.removeAll();
            panelExit();
            stock.setSize(0, 0);
            stock.setLocation(0, 0);
            jDesktopPane1.removeAll();
            stock.setVisible(false);
            title = "";
        }

        hideMenu(evt);

    }//GEN-LAST:event_lblStockMouseClicked

