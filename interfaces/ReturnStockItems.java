    Vector<String> getSupplierDetails(){
        Vector v = new Vector();
        v.add("Cannot find!");
        v.add("Cannot find!");
        v.add("Cannot find!");
        v.add("Cannot find!");
        v.add("Cannot find!");
        try {
            ResultSet r = new JDBC().getData("SELECT * FROM suppliers WHERE sup_id=(SELECT sup_id FROM grn_info WHERE grn_no=(SELECT grn_no FROM grn WHERE serial='"+serial+"' ))");
            if (r.next()) {
                v.removeAllElements();
                v.add(r.getString("sup_id"));
                v.add(r.getString("sup_name"));
                v.add(r.getString("sup_tel"));
                v.add(r.getString("sup_address"));
                if (r.getString("sup_web").isEmpty()) v.add("Not Available");
                else v.add(r.getString("sup_web"));                
            }        
            return v;
        } catch (Exception e) {
            e.printStackTrace();
            return v;
        }
        
    }
