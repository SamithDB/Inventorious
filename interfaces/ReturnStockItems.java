    private void btn_returnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_returnActionPerformed

        int ReturnQty = Integer.parseInt(R_qty.getValue().toString());
        if (ReturnQty>0){
            if (ReturnQty<=qty) {
                int returnIt = JOptionPane.showConfirmDialog(this,"Are you sure want to return this item ?","Return",0);
                if (returnIt==0) {
                    String date = new SimpleDateFormat("yyyy-MM-dd hh:mm:ss").format(new Date());
                    
                    try {
                        new JDBC().putData("INSERT INTO returns VALUES('"+nextID()+"','"+serial+"','FROM_STOCK','"+ReturnQty+"','"+getSupplierDetails().get(0)+"','"+date+"','SUPPLIER','"+txt_reason.getText()+"')");
                        new JDBC().putData("UPDATE STOCK SET qty=qty-'"+ReturnQty+"' WHERE serial='"+serial+"' ");
                        refreshQty();
                        JOptionPane.showMessageDialog(this,"Returned Successfully ");
                    } catch (Exception e) {
                        JOptionPane.showMessageDialog(this,"Error while returning item !");
                        e.printStackTrace();
                    }
                }
            }else{
                JOptionPane.showMessageDialog(this, "Cannot return more than stock items");
            }
        }else{
            JOptionPane.showMessageDialog(this, "Quantity must be at least 0");
        }
        
        
       
    }//GEN-LAST:event_btn_returnActionPerformed

    void refreshQty() throws Exception{
        ResultSet r = new JDBC().getData("SELECT qty FROM STOCK WHERE serial='"+serial+"' ");
        if (r.next()){
            if(r.getInt("qty")>0) {
                qty =r.getInt("qty");
                txt_qty.setForeground(new Color(0,120,0));
                txt_qty.setText(String.valueOf(r.getInt("qty")) + " Available");
                ViewStock.setQty(serial, r.getInt("qty"));
            }else{
                txt_qty.setForeground(new Color(204,0,102));
                txt_qty.setText("Not Available !");
                qty =r.getInt("qty");
                ViewStock.setQty(serial, r.getInt("qty"));
            }
        }
        
    }
    int nextID() throws Exception{
        ResultSet r = new JDBC().getData("SELECT MAX(return_id) AS NEXT FROM returns");
        if (r.next()) {
          return r.getInt("NEXT")+1;  
        }else{
            return 1;
        }        
    }   
