    RoundedBalloonStyle style = new RoundedBalloonStyle(10, 7, Color.yellow, Color.yellow);
    private void passCurrentKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_passCurrentKeyPressed

        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if (String.valueOf(passCurrent.getPassword()).equals(pass)) {
                passNewPass.grabFocus();
                passNewPass.setEnabled(true);
                passConfirmPass.setEnabled(true);

            } else {
                passNewPass.setEnabled(false);
                passConfirmPass.setEnabled(false);
                BalloonTip tip = new BalloonTip(passCurrent, "Your current password does not match.", style, false);
                tip.setVisible(true);
                TimingUtils.showTimedBalloon(tip, 3000);
                passCurrent.setText("");
                passCurrent.grabFocus();

            }
        }
    }//GEN-LAST:event_passCurrentKeyPressed

    private void passNewPassKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_passNewPassKeyPressed

        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if (String.valueOf(passNewPass.getPassword()).length() > 6) {
                passConfirmPass.grabFocus();
                passConfirmPass.setEnabled(true);

            } else {
                passConfirmPass.setEnabled(false);
                BalloonTip tip = new BalloonTip(passNewPass, "Please enter more than 6 characters.", style, false);
                tip.setVisible(true);
                TimingUtils.showTimedBalloon(tip, 3000);
                passNewPass.grabFocus();

            }
        }
    }//GEN-LAST:event_passNewPassKeyPressed

    private void passConfirmPassKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_passConfirmPassKeyPressed

        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if (String.valueOf(passConfirmPass.getPassword()).equals(String.valueOf(passConfirmPass.getPassword()))) {
                btnChangePass.setEnabled(true);

            } else {
                passConfirmPass.setEnabled(false);
                BalloonTip tip = new BalloonTip(passNewPass, "Confirm password not matched. Plese enter correct Password.", style, false);
                tip.setVisible(true);
                TimingUtils.showTimedBalloon(tip, 3000);
                passConfirmPass.grabFocus();

            }
        }
    }//GEN-LAST:event_passConfirmPassKeyPressed

