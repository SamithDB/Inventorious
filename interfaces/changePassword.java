    private void btnChangePassActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChangePassActionPerformed

        if (String.valueOf(passNewPass.getPassword()).equals(String.valueOf(passConfirmPass.getPassword()))) {
            try {
                new JDBC().putData("UPDATE user SET passWord='" + String.valueOf(passNewPass.getPassword()) + "', tip='" + txtPassTip.getText() + "' WHERE userName='" + Home.userName + "'");
                this.dispose();
            } catch (Exception e) {
                e.printStackTrace();
            }
        } else {
            BalloonTip tip = new BalloonTip(passConfirmPass, "Confirm password not matched. Plese enter correct Password.", style, false);
            tip.setVisible(true);
            TimingUtils.showTimedBalloon(tip, 3000);
        }
    }//GEN-LAST:event_btnChangePassActionPerformed


