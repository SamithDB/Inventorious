    private void lnkRemoveUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_lnkRemoveUserActionPerformed

        int ii = JOptionPane.showConfirmDialog(this, "Are you sure to remove this user?", "Confirm", JOptionPane.YES_NO_CANCEL_OPTION);
        if (ii == 0) {
            int i = tblUserTabel.getSelectedRow();
            String user = tblUserTabel.getValueAt(i, 0).toString();
            try {
                new JDBC().putData("DELETE FROM user WHERE userId='" + user + "'");
                getUserinfo();
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_lnkRemoveUserActionPerformed

