    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        DefaultTableModel dtm = (DefaultTableModel) tblUserTabel.getModel();
        int count = tblUserTabel.getRowCount();
        for (int i = 0; i < count; i++) {
            dtm.removeRow(0);
        }
        try {
            ResultSet r = new JDBC().getData("SELECT * FROM user");
            while (r.next()) {
                Vector v = new Vector();
                v.add(r.getString("userId"));
                v.add(r.getString("userName"));
                v.add(r.getString("passWord"));
                v.add(r.getString("level"));
                dtm.addRow(v);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_jButton1ActionPerformed

