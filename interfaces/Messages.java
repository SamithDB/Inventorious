    private void jTree1PropertyChange(java.beans.PropertyChangeEvent evt) {//GEN-FIRST:event_jTree1PropertyChange

        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        int i = jTable1.getRowCount();
        for (int j = 0; j < i; j++) {
            dtm.removeRow(0);
        }
        String sql = "SELECT * FROM message WHERE m_status='Not Read' and receiver='" + Home.userName + "'";
        DefaultMutableTreeNode treeNode = (DefaultMutableTreeNode) jTree1.getLastSelectedPathComponent();
        if (treeNode != null) {
            String option = treeNode.toString();
            if (option.equals("Inbox")) {
                sql = "SELECT * FROM message WHERE receiver='" + Home.userName + "'";
                lblTitle.setText("Inbox");

            } else if (option.equals("Un-Read")) {
                sql = "SELECT * FROM message WHERE m_status='Not Read' and receiver='" + Home.userName + "'";
                lblTitle.setText("Un-Read Messages");

            } else if (option.equals("Saved")) {
                sql = "SELECT * FROM message WHERE m_status='Saved' and sender='" + Home.userName + "'";
                lblTitle.setText("Saved Messages");

            } else if (option.equals("Sent")) {
                sql = "SELECT * FROM message WHERE sender='" + Home.userName + "'";
                lblTitle.setText("Sent Messages");

            }
        }
        try {
            ResultSet r = new JDBC().getData(sql);
            while (r.next()) {
                Vector v = new Vector();
                v.add(r.getString("sender"));
                v.add(r.getString("datetime"));
                v.add(r.getString("message"));
                dtm.addRow(v);

            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jTree1PropertyChange

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked

        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        int i = jTable1.getSelectedRow();
        String sender = (String) dtm.getValueAt(i, 0);
        String date = (String) dtm.getValueAt(i, 1);
        String message = (String) dtm.getValueAt(i, 2);

        if (dtm.getValueAt(i, 0).equals("Olympus")) {
            btnReply.setEnabled(false);
        } else {
            btnReply.setEnabled(true);
            btnForward.setEnabled(true);

        }

        txtMessage.setText(sender + "\n" + "\n" + date + "\n" + "\n" + message);
        try {
            new JDBC().putData("UPDATE message SET m_status='Read' WHERE datetime='" + date + "'");
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jTable1MouseClicked

