    public void autoSuggest() {
        try {
            Class.forName("com.mysql.jdbc.Connection");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/build_final", "root", "1234");
            ResultSet r = con.createStatement().executeQuery("SELECT item_des FROM items where item_id IN(select DISTINCT(item_id) from stock where qty>0)");

            jComboBox2.removeAllItems();
            r.first();
            if (jComboBox2.getItemCount() == 0) {
                do {
                    jComboBox2.addItem(r.getString("item_des"));
                    v.addElement(r.getString("item_des"));
                    jComboBox2.addItemListener(new ItemListener() {

                        @Override
                        public void itemStateChanged(ItemEvent ie) {
                            if (ie.getStateChange() == ItemEvent.SELECTED) {
                                jComboBox2.getSelectedIndex();
                            }
                        }
                    });

                } while (r.next());
            } else {
                jComboBox2.addItem("NOOOOO");
            }
        } catch (Exception e) {
            e.getMessage();
        }
        jComboBox2.setEditable(true);
        tx = (JTextField) jComboBox2.getEditor().getEditorComponent();
        tx.addKeyListener(new KeyAdapter() {
            @Override
            public void keyTyped(KeyEvent evt) {
                EventQueue.invokeLater(new Runnable() {

                    @Override
                    public void run() {
                        String text = tx.getText().toUpperCase();
                        if (text.length() == 0) {
                            jComboBox2.hidePopup();
                            setModel(new DefaultComboBoxModel(v), "");
                        } else {
                            DefaultComboBoxModel m = getSuggest(v, text);
                            if (m.getSize() == 0) {
                                jComboBox2.hidePopup();
                            } else {
                                setModel(m, text);
                                jComboBox2.showPopup();
                            }
                        }
                    }
                });

            }

            @Override
            public void keyPressed(KeyEvent e) {
                String text = tx.getText();
                int code = e.getKeyCode();
                if (code == KeyEvent.VK_ENTER) {
                    if (v.contains(text)) {
                        // v.addElement(text);
                        jComboBox2.hidePopup();
                        jComboBox2.setSelectedItem(text);
                    }
                    hide_flag = true;
                } else if (code == KeyEvent.VK_ESCAPE) {
                    hide_flag = true;
                } else if (code == KeyEvent.VK_RIGHT) {
                    for (int i = 0; i < v.size(); i++) {
                        String str = (String) v.elementAt(i);
                        if (str.startsWith(text)) {
                            tx.setText(str);
                        }
                    }
                }
            }
        });

    }

    private void setModel(DefaultComboBoxModel mdl, String str) {
        jComboBox2.setModel(mdl);
        tx.setText(str);
    }

    private DefaultComboBoxModel getSuggest(List<String> list, String text) {
        DefaultComboBoxModel m = new DefaultComboBoxModel();
        for (String s : list) {
            if (s.startsWith(text)) {
                m.addElement(s);
            }
        }
        return m;
    }

