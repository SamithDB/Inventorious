    private boolean isExist(String serial) {
        boolean cont = true;
        int rows = jTable1.getRowCount();
        for (int i = 0; i < rows; i++) {
            String se = jTable1.getValueAt(i, 0).toString();
            if (se.equals(serial)) {
                JOptionPane.showMessageDialog(this, "This item is Already Added !");
                cont = false;
            }
        }
        return cont;
    }
