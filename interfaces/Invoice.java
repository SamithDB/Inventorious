    private void save() {
        try {
            new JDBC().putData("insert into invoice values('" + txt_invoiceno.getText() + "','" + txt_cusid.getText() + "','" + txt_rep.getText() + "','" + jComboBox1.getSelectedItem() + "','" + txt_payment.getText() + "','" + jTextField2.getText() + "','" + "Sold" + "')");
            JOptionPane.showMessageDialog(this, "Saved !");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "cant Save ! "
                    + "Check the information again...");
            e.printStackTrace();
        }

        int rowc = jTable1.getRowCount();
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        for (int i = 0; i < rowc; i++) {
            String serial = (String) dtm.getValueAt(i, 0);
            String item_des = (String) dtm.getValueAt(i, 1);

            try {
                ResultSet r = new JDBC().getData("SELECT * FROM stock where serial='" + serial + "'");
                while (r.next()) {
                    int qty_stock = Integer.parseInt(r.getString("qty"));
                    int qty1 = Integer.parseInt(jTable1.getValueAt(i, 2).toString());
                    int new_qty = qty_stock - qty1;
                    new JDBC().putData("UPDATE stock SET qty='" + new_qty + "' where serial='" + dtm.getValueAt(i, 0) + "'");
                }

                new JDBC().putData("INSERT INTO invoice_info VALUES('" + txt_invoiceno.getText() + "','" + serial + "','" + item_des + "','" + dtm.getValueAt(i, 2) + "','" + dtm.getValueAt(i, 3) + "','" + dtm.getValueAt(i, 5) + "','" + "Sold" + "')");

            } catch (Exception e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "cant Save ! "
                        + "Check the information again...");
            }
        }
        JOptionPane.showMessageDialog(this, "Saved ! report is processing");
    }

