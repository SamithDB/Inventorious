    public void oldinvoice(String id){
        
        DefaultTableModel dtm=(DefaultTableModel)jTable1.getModel();
        
                    int rowcount=jTable1.getRowCount();
        
                    for (int i = 0; i <rowcount; i++) {
                     dtm.removeRow(0);
                    }
        try {
            ResultSet r = new JDBC().getData("SELECT sk.*,it.* FROM invoice_info AS sk,invoice AS it where  sk.invoice_no = it.invoice_no AND sk.invoice_no LIKE'"+id+"'");
            while(r.next()){
                Vector v=new Vector();
                v.add(r.getString("item_id"));
                v.add(r.getString("item_description")); 
                v.add(r.getString("qty_new")); 
                v.add(r.getDouble("unit_price"));
                double amount=r.getDouble("qty_new")*r.getDouble("unit_price");
                v.add(4, amount);
                v.add(5, r.getDouble("discount"));
                v.add(6, r.getString("status"));
                dtm.addRow(v);
                
                txt_invoiceno.setText(r.getString("invoice_no"));
                txt_rep.setText(r.getString("rep_id"));
                jTextField2.setText(r.getString("date_time"));
                txt_cusid.setText(r.getString("customer_id"));
                txt_method.setText(r.getString("method"));
                
                txt_payment.setText(r.getString("payment"));
                
                int  rows = jTable1.getRowCount();
                
        double total=0;
        for (int i = 0;  i < rows; i++) 
 
        {total += ( Double.parseDouble(jTable1.getValueAt(i, 4).toString())) ;}
        
        String s=total+"";
        txt_subtotal.setText(s);
        
        double discount=0;
        for (int i = 0;  i < rows; i++) 
 
        {discount += ( Double.parseDouble(jTable1.getValueAt(i, 5).toString())) ;}
        
        String s1=discount+"";
        txt_discount.setText(s1);
        
        double nettotal=total-discount;
        String s2=nettotal+"";
        txt_nettotal.setText(s2);
        
        double d=Double.parseDouble(txt_payment.getText());
        double d1=d-nettotal;
        String s3=d1+"";
        txt_balance.setText(s3);
      
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
