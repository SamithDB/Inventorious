        DefaultTableModel dtm=(DefaultTableModel)jTable1.getModel();
        int i=jTable1.getSelectedRow();
        String s1=jTable1.getValueAt(i, 2).toString();
        String s=jTextField3.getText();
        
        int qty_sold=Integer.parseInt(s1); 
        int qty_return=Integer.parseInt(txt_qty.getText());
