    String getSupId(String serial){
        String id="SUP000001";
        try {
            ResultSet r = new JDBC().getData(" select sup_id from suppliers where sup_id IN(select sup_id from grn_info where grn_no IN(select grn_no from grn where serial='"+serial+"'))");
            if (r.next()) {
                id = r.getString("sup_id");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
            return id;
    }

