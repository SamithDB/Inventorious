                    new JDBC().putData("INSERT INTO returns VALUES('"+return_id+"','"+s+"','"+txt_invoiceno.getText()+"','"+qty_return+"','"+getSupId(s)+"','"+new SimpleDateFormat("yyyy-MM-dd hh:mm:ss").format(new Date())+"','"+jComboBox2.getSelectedItem()+"','"+txt_reason.getText()+"')");
