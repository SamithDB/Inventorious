    public void supid(){
        try {
            ResultSet r=new JDBC().getData("SELECT COUNT(sup_id) AS row_count FROM suppliers");
            while(r.next()){
                int row=Integer.parseInt(r.getString("row_count"));
                int id=row+1;
                String sid="SUP00000"+id;
                txt_sup_id.setText(sid);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
