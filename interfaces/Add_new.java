    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed
        supid();
        
            Date d = new Date();
            SimpleDateFormat s = new SimpleDateFormat("yyyy-MM-dd");
            final String dt = s.format(d);
        
        try {
            String gen="";
            if(jRadioButton1.isSelected()){gen="Male";}else if(jRadioButton2.isSelected()){gen="Female";}
            new JDBC().putData("INSERT INTO suppliers VALUES('"+txt_sup_id.getText()+"','"+txt_sup_fname.getText()+"_"+txt_sup_lname.getText()+"','"+txt_sup_address_no.getText()+"_"+txt_sup_address_street.getText()+"_"+txt_sup_address_city.getText()+"','"+txt_sup_mobile.getText()+"','"+txt_sup_tel.getText()+"','"+txt_sup_email.getText()+"','"+txt_sup_web.getText()+"','"+txt_sup_fax.getText()+"','"+txt_sup_company.getText()+"','"+txt_sup_company_address.getText()+"','"+txt_sup_company_contactnum1.getText()+"_"+txt_sup_company_contactnum2.getText()+"','"+txt_sup_nic.getText()+"','"+dt+"','"+"Activate"+"','"+gen+"')");
            JOptionPane.showMessageDialog(this, "Saved !");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "cant Save ! "
                    + "Check the information again...");
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton9ActionPerformed

