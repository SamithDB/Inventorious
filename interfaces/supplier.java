    private void tbl_supplierMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_supplierMouseClicked
        selectedrow=tbl_supplier.getSelectedRow();
        DefaultTableModel dtm=(DefaultTableModel)tbl_supplier.getModel();
        String id=(String)dtm.getValueAt(selectedrow,0);
        sid.setText(id);

        String nm=(String)dtm.getValueAt(selectedrow,1);
        sname.setText(nm);

        String nic=(String)dtm.getValueAt(selectedrow,2);
        snic.setText(nic);

        String com=(String)dtm.getValueAt(selectedrow,3);
        scompany.setText(com);

        String st=(String)dtm.getValueAt(selectedrow,5);

        if("Activate".equals(st)){
            jToggleButton1.setText("Deactivate");
            jToggleButton1.setSelected(false);
        }else if("Deactivate".equals(st)){
            jToggleButton1.setText("Activate");
            jToggleButton1.setSelected(true);
        }

        try {
            ResultSet r = new JDBC().getData("SELECT * FROM suppliers where sup_id='"+sid.getText()+"'");
            while(r.next()){

                scontactnumber.setText(r.getString("sup_mobile"));
                scompanyaddress.setText(r.getString("sup_company_address"));
                semail.setText(r.getString("sup_email"));
                swebsite.setText(r.getString("sup_web"));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,"Error for mouseclicked code");
        }

        btn_more.setEnabled(true);
        btn_edit.setEnabled(true);
        btn_grn.setEnabled(true);
        btn_payments.setEnabled(true);
        btn_product_refresh.setEnabled(true);
        jToggleButton1.setEnabled(true);
        
        pro();

    }//GEN-LAST:event_tbl_supplierMouseClicked

