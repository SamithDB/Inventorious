    void pro(){
         int rowcount=jTable1.getRowCount();
        DefaultTableModel dtm=(DefaultTableModel)jTable1.getModel();
        for (int i = 0; i <rowcount; i++) {
            dtm.removeRow(0);
        }
        try {
            ResultSet r1 = new JDBC().getData("select s.serial, s.qty , i.item_des from stock s,items i where i.item_id=s.item_id AND s.serial IN(select serial from grn where grn_no IN(select grn_no from grn_info where sup_id='"+sid.getText()+"')) ");
            while(r1.next()){
                    
                    Vector v1=new Vector();
                    v1.add(r1.getString("s.serial"));
                    v1.add(r1.getString("i.item_des"));
                    v1.add(r1.getString("s.qty"));
                    dtm.addRow(v1);
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this,"No products in stock...");
        }
    }
