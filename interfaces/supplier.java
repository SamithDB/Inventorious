    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        DefaultTableModel dtm=(DefaultTableModel)tbl_supplier.getModel();
        String st=(String)dtm.getValueAt(selectedrow,5);

        if("Activate".equals(st)){
            try {
                new JDBC().putData("update suppliers set sup_status='"+"Deactivate"+"' where sup_id='"+sid.getText()+"'");
                JOptionPane.showMessageDialog(this, "Supplier Deactivated.....", "Info", JOptionPane.INFORMATION_MESSAGE);
            } catch (Exception e) {
                e.printStackTrace();
            }
        }else if("Deactivate".equals(st)){
            try {
                new JDBC().putData("update suppliers set sup_status='"+"Activate"+"' where sup_id='"+sid.getText()+"'");
                JOptionPane.showMessageDialog(this, "Supplier Aactivated.....", "Info", JOptionPane.INFORMATION_MESSAGE);
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
        ref();
    }//GEN-LAST:event_jToggleButton1ActionPerformed

