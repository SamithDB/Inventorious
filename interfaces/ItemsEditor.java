    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        getMerge();
        if (!combo_cate.getSelectedItem().toString().isEmpty() && !combo_model.getSelectedItem().toString().isEmpty() && !txt_des.getText().isEmpty()) {
            try {
                ResultSet r = new JDBC().getData("SELECT * FROM items WHERE item_des='"+getMerge()+"' ");
                int conf=1;
                if (r.next()) {
                    conf = JOptionPane.showConfirmDialog(this, "This item is already in your Store\n are you sure want to apply these changes !");
                } else {
                    conf = JOptionPane.showConfirmDialog(this,"By Clicking Apply button Chaging all Details about these item \n Are you sure want to Apply these changes.","Save Changes",0);
                }
                if (conf==0) {
                        new JDBC().putData("UPDATE items SET item_model='"+combo_model.getSelectedItem()+"',item_cate='"+combo_cate.getSelectedItem()+"',item_des='"+getMerge()+"' WHERE item_id='"+item_id+"'  ");
                        JOptionPane.showMessageDialog(this, "Successfully Applied !");
                        setTableContent();
                        dispose();
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
            
        }else{
           JOptionPane.showMessageDialog(this, "To continue, you need to fill all fields !");
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

