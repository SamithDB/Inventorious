    void setTableContent(){
        int rows = ViewItems.table_viewItems.getRowCount();
        for (int i = 0; i < rows; i++) {
            if (ViewItems.table_viewItems.getValueAt(i, 0).equals(item_id)) {
                ViewItems.table_viewItems.setValueAt(combo_model.getSelectedItem(), i, 1);
                ViewItems.table_viewItems.setValueAt(combo_cate.getSelectedItem(), i, 2);
                ViewItems.table_viewItems.setValueAt(getMerge(), i, 3);
            }
        }
    }
    String getMerge(){
        String merge = combo_model.getSelectedItem()+" "+combo_cate.getSelectedItem()+" "+txt_des.getText();
        item_des.setText(merge.toUpperCase());
        return merge.toUpperCase();
    }
    
    void setDetails(String id,String model,String cate,String des){
        item_id=id;
        txt_itemID.setText(id);
        setModels();
        combo_model.setSelectedItem(model);
        combo_cate.setSelectedItem(cate);
        
        String des2 = des.replaceAll(model, "").replaceFirst("\\s+", "").replaceAll(cate, "").replaceFirst("\\s+", "");
        txt_des.setText(des2);
        getMerge();
    }
    void setModels(){
        try {
            JDBC Data = new JDBC();
            ResultSet r = Data.getData("SELECT DISTINCT(item_model) FROM items");
            ResultSet r2 = Data.getData("SELECT DISTINCT(item_cate) FROM items");
            
            Vector models = new Vector();
            Vector cate = new Vector();
            while (r.next()) {                
                models.add(r.getString("item_model"));
            }
            while (r2.next()) {                
                cate.add(r2.getString("item_cate"));
            }
            combo_model.setModel(new DefaultComboBoxModel(models));
            combo_cate.setModel(new DefaultComboBoxModel(cate));
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

